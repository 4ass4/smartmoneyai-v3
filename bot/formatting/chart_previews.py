def generate_chart_preview_url(symbol, timeframe):
    """
    Генерация URL для превью графика
    
    Args:
        symbol: символ
        timeframe: таймфрейм
        
    Returns:
        URL превью
    """
    # Упрощенная версия - в реальности нужна интеграция с сервисом графиков
    return f"https://example.com/chart/{symbol}/{timeframe}"

